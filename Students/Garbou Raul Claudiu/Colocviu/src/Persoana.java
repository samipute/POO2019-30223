
public class Persoana {
	int id;
	String nume;
	int numarTelefon;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getNume() {
		return nume;
	}

	public void setNume(String nume) {
		this.nume = nume;
	}

	public int getNumarTelefon() {
		return numarTelefon;
	}

	public void setNumarTelefon(int numarTelefon) {
		this.numarTelefon = numarTelefon;
	}
}
